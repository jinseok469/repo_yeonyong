package com.mickey.demo.fapp.address;

public class AddressDto {
	
	private String seq;
	private String user_seq;
	private String postalCode;
	private String betterAddress;
	private String addressKind;
	private Integer leaderNumber;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getUser_seq() {
		return user_seq;
	}
	public void setUser_seq(String user_seq) {
		this.user_seq = user_seq;
	}
	public String getPostalCode() {
		return postalCode;
	}
	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}
	public String getBetterAddress() {
		return betterAddress;
	}
	public void setBetterAddress(String betterAddress) {
		this.betterAddress = betterAddress;
	}
	public String getAddressKind() {
		return addressKind;
	}
	public void setAddressKind(String addressKind) {
		this.addressKind = addressKind;
	}
	public Integer getLeaderNumber() {
		return leaderNumber;
	}
	public void setLeaderNumber(Integer leaderNumber) {
		this.leaderNumber = leaderNumber;
	}
	
	

}
