package com.mickey.demo.fapp.dob;

public class DobDto {
	private String seq;
	private String day;
	private String user_seq;
	private String anniversaryCategory;
	private Integer leaderNumber;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getUser_seq() {
		return user_seq;
	}
	public void setUser_seq(String user_seq) {
		this.user_seq = user_seq;
	}
	public String getAnniversaryCategory() {
		return anniversaryCategory;
	}
	public void setAnniversaryCategory(String anniversaryCategory) {
		this.anniversaryCategory = anniversaryCategory;
	}
	public Integer getLeaderNumber() {
		return leaderNumber;
	}
	public void setLeaderNumber(Integer leaderNumber) {
		this.leaderNumber = leaderNumber;
	}
	
	
}
